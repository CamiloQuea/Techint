package model;

/**
 *
 * @author Camilo
 */
public class Empresa {

    private String id;
    private String ruc;
    private String nombre;

    public Empresa(String id, String ruc, String nombre) {
        this.id = id;
        this.ruc = ruc;
        this.nombre = nombre;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRuc() {
        return ruc;
    }

    public void setRuc(String ruc) {
        this.ruc = ruc;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

}
