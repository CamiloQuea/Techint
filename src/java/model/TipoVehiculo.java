package model;

/**
 *
 * @author Camilo
 */
public class TipoVehiculo {
    
    String id;
    String cod;
    String detalle;

    public TipoVehiculo(String id, String cod, String detalle) {
        this.id = id;
        this.cod = cod;
        this.detalle = detalle;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCod() {
        return cod;
    }

    public void setCod(String cod) {
        this.cod = cod;
    }

    public String getDetalle() {
        return detalle;
    }

    public void setDetalle(String detalle) {
        this.detalle = detalle;
    }
    
    
}
